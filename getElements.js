/**
 *
 * @param id
 * @returns {HTMLElement}
 * @author 管忠旭
 */
function $MyId(id){
    return document.getElementById(id);
}
/**
 *
 * @param class
 * @returns {HTMLElement}
 * @author 管忠旭
 */
function $MyClass(Class){
    return document.getElementsByClassName(Class);
}
/**
 *
 * @param label
 * @returns {HTMLElement}
 * @author 管忠旭
 */
function $MyTag(Tag){
    return  document.getElementsByTagName(Tag);
}
